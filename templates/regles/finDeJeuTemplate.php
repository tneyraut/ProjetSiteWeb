<h4 style="text-decoration: underline">Fin de partie</h4>

Lorsque la réserve de wagons d’un joueur est de 0, 1 ou 2 wagons après avoir joué son tour, chaque joueur, en incluant celui-ci, 
joue encore un tour. À l’issue de ce dernier tour, le jeu s’arrête et chacun compte ses points.

<h4 style="text-decoration: underline">Calcul des points</h4>

Chaque route construite rapporte des points selon sa longueur :
<ul>
    <li><strong>Longueur 1 : </strong>1 point</li>
    <li><strong>Longueur 2 : </strong>2 points</li>
    <li><strong>Longueur 3 : </strong>4 points</li>
    <li><strong>Longueur 4 : </strong>7 points</li>
    <li><strong>Longueur 5 : </strong>10 points</li>
    <li><strong>Longueur 6 : </strong>15 points</li>
</ul>

Chaque carte Destination fait référence à deux villes de la carte et un nombre de points y est associé. Si le joueur réalise la 
connexion entre les deux villes d’une carte Destination, il remporte le nombre de points indiqué sur la carte et l’additionne, 
en fin de partie, aux points déjà acquis. Si la connexion n’est pas réalisée, le joueur déduit de son nombre de points déjà acquis
le nombre indiqué sur la carte.<br><br>

Enfin, le joueur qui a le chemin continu le plus long reçoit la carte bonus du chemin le plus long et marque 10 points supplémentaires. 
Les boucles sont autorisées (le chemin le plus long peut passer plusieurs fois par la même ville) mais en aucun cas un même wagon ne peut
être comptabilisé ni utilisé deux fois. En cas d'égalité entre plusieurs joueurs, tous les joueurs concernés marquent 10 points.
Le joueur qui a le plus de points est déclaré vainqueur. En cas d’égalité entre plusieurs joueurs, le joueur qui a complété le plus de
cartes Destination remporte la victoire.