En début de partie, chaque joueur possède 30 jetons correspondant à sa couleur ainsi que 4 cartes Wagon. 
Une pioche de cartes wagon est disposée à côté du plateau et les cinq premières
cartes de la pioche sont face visible.<br><br>

Chaque joueur pioche 3 cartes destinations et doit décider lesquelles conserver. Il doit
garder au moins 2 cartes, mais peut conserver les 3 s’il le souhaite. Le paquet de cartes Destination est
ensuite placé face cachée à côté du plateau. Les joueurs gardent leurs destinations
secrètes jusqu'à la fin du jeu.<br><br>

