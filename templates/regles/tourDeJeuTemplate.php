À son tour, le joueur doit faire une et une seule des trois actions suivantes :<br><br>
<ol>
    <li><strong>Prendre des cartes Wagon</strong> – le joueur peut prendre 2 cartes Wagon. Il peut prendre n’importe quelle carte 
        visible parmi les 5 posées surla table ou tirer une carte du dessus de la pioche (tirage en aveugle). Si le joueur prend 
        une carte visible, elle est immédiatement remplacée par une autre du dessus de la pioche. Il peut ensuite prendre une deuxième 
        carte, soit visible, soit en aveugle.<br>
        <img class="carteRegle" src="<?php $this->bu() ?>images/cartes/starwars/pioche.png" />
        <img class="carteRegle" src="<?php $this->bu() ?>images/cartes/pokemon/pioche.png" /><br><br>
    </li>
    
    <li><strong>Prendre possession d’une route</strong> – Le joueur peut s'emparer d'une route sur le plateau en utilisant autant de 
        cartes Wagon de la couleur de la route que d’espaces composant la route. Il est possible d'utiliser des cartes joker qui 
        peuvent remplacer n'importe quelle couleur.<br><br>
    </li>
    
    <li><strong>Prendre des cartes Destination supplémentaires</strong> – Le joueur prend 3 cartes Destination du dessus de la pioche. 
        Il doit en conserver au moins une, mais peut aussi garder 2 ou 3 cartes.<br>
