Il s'agit d'un jeu de plateau se jouant de 2 à 5 joueurs.<br>
Le plateau se compose de deux éléments : des villes (ou planètes) et des routes reliants les villes entre elles.
<br><br>
L'objectif de chaque joueur est de marquer le maximum de points.
Le but du jeu est d’obtenir le plus de points. On gagne des points de la manière suivante :
<ol>
 <li>En capturant une route entre 2 villes.</li>
 <li>En reliant par une route, en continu, les deux villes d’une carte Destination.</li>
 <li>En réalisant le chemin le plus long.</li>
</ol>
Les points sont enlevés, et non gagnés, si deux villes d’une carte Destination conservée par le joueur ne sont pas reliées en continu à la fin du jeu.